package Cadastro;

public class produtoEGarantia {
    private String tipoProduto;
    private String modeloProduto;
    private String descricaoDetalhadaProduto;
    private String dataAteGarantia;

    public String getTipoProduto() {
        return tipoProduto;
    }

    public void setTipoProduto(String tipoProduto) {
        this.tipoProduto = tipoProduto;
    }

    public String getModeloProduto() {
        return modeloProduto;
    }

    public void setModeloProduto(String modeloProduto) {
        this.modeloProduto = modeloProduto;
    }

    public String getDescricaoDetalhadaProduto() {
        return descricaoDetalhadaProduto;
    }

    public void setDescricaoDetalhadaProduto(String descricaoDetalhadaProduto) {
        this.descricaoDetalhadaProduto = descricaoDetalhadaProduto;
    }

    public String getDataAteGarantia() {
        return dataAteGarantia;
    }

    public void setDataAteGarantia(String dataAteGarantia) {
        this.dataAteGarantia = dataAteGarantia;
    }
}
